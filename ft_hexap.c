#include "ft_printf.h"

int	ft_hexap(unsigned long long p)
{
	int		l;
	char	*s;

	l = 0;
	s = "0123456789abcdef";
	if (p >= 16)
		l += ft_hexap(p / 16);
	l += ft_putchar(s[p % 16]);
	return (l);
}
