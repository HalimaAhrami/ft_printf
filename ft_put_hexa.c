#include "ft_printf.h"

int	ft_put_hexa(unsigned int nbr)
{
	char	*s;
	int		l;

	s = "0123456789abcdef";
	l = 0;
	if (nbr >= 16)
		l += ft_put_hexa(nbr / 16);
	l += ft_putchar(s[nbr % 16]);
	return (l);
}
