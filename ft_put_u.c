#include "ft_printf.h"

int	ft_put_u(unsigned int nbr)
{
	int	l;

	l = 0;
	if (nbr >= 10)
		l += ft_put_u(nbr / 10);
	l += ft_putchar(nbr % 10 + '0');
	return (l);
}
