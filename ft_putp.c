#include "ft_printf.h"

int	ft_putp(unsigned long long p)
{
	int	l;

	l = 0;
	if (p == 0)
		l += ft_putstr("(nil)");
	else
	{
		l += ft_putstr("0x");
		l += ft_hexap(p);
	}
	return (l);
}
