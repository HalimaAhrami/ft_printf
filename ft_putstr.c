#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	l;

	l = 0;
	if (s == NULL)
		return (write(1, "(null)", 6));
	while (*s)
	{
		l += ft_putchar(*s);
		s++;
	}
	return (l);
}
